class Demo{
      public static void main(String [] args){
           int a = 10;
           int b = 100;
           b += 20;
           a = a + 10;
           a = a * 10;
           b = b % 5;
           b = b / 2;
          
          
      
           System.out.println(a);
           System.out.println(b);
           System.out.println(a++);
           System.out.println(++a);
           System.out.println(b--);
           System.out.println(--b);
      }

}
